<?php

$num1; $num2; $operator;
    switch ($operator) {
        case "+":
           echo $num1 + $num2;
            break;
        case "-":
            echo  $num1 - $num2;
            break;
        case "*":
            echo  $num1 * $num2;
            break;
        case "/":
            echo  $num1 / $num2;
    }
    
?>
